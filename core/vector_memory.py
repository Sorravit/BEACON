    async def store_auto_fact(
        self, topic: str, fact: str, confidence: str = "medium"
    ) -> Optional[bool]:
        """Store an auto-extracted fact; updates existing entry if topic matches."""
        if not await self.ensure_ready():
            return None
        if not self._is_grpc_reachable():
            await self._mark_unavailable()
            return None
        try:
            # ✅ ASYNC: was self.client.collections.get()  (sync)
            collection = self._client.collections.get("AutoLearned")
            # ✅ ASYNC: was collection.query.fetch_objects()  (sync)
            existing = await collection.query.fetch_objects(
                limit=200, return_properties=["topic", "fact"]
            )
            lower_topic = topic.lower()
            for obj in existing.objects:
                if (obj.properties.get("topic") or "").lower() == lower_topic:
                    # ✅ ASYNC: was collection.data.update()  (sync)
                    await collection.data.update(
                        uuid=obj.uuid,
                        properties={
                            "fact": fact,
                            "confidence": confidence,
                            "updated_at": datetime.now().isoformat(),
                        },
                    )
                    logger.info(f"Updated auto-fact [{topic}]")
                    return True
            vector = await self._embed(f"{topic}: {fact}", label="Auto-learning")
            if vector is None:
                return False
            # ✅ ASYNC: was collection.data.insert()  (sync)
            await collection.data.insert(
                properties={
                    "topic":      topic,
                    "fact":       fact,
                    "confidence": confidence,
                    "stored_at":  datetime.now().isoformat(),
                    "updated_at": datetime.now().isoformat(),
                },
                vector=vector,
            )
            logger.info(f"Stored auto-fact [{topic}]: {fact[:80]}")
            return True
        except Exception as exc:
            logger.error(f"store_auto_fact failed: {exc}")
            await self._mark_unavailable()
            return None

